void PrintImmage (int[,] arr)
{
    for (int i = 0; i < arr.GetLength(0); i++)
    {
        for (int j = 0; j < arr.GetLength(1); j++)
        {
            //Console.Write($"{arr[i,j]} ");
            if (arr[i,j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImmage (int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImmage (row-1, col);
        FillImmage (row, col-1);
        FillImmage (row+1, col);
        FillImmage (row, col+1);
    }
}

